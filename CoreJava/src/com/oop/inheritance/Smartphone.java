package com.oop.inheritance;

public class Smartphone {
	private int ram = 0;
	private int rom = 0;
	private int camera = 0;
	private String procesesor = null;

	public int getRam() {
		return ram;
	}

	public void setRam(int ram) {
		this.ram = ram;
	}

	public int getRom() {
		return rom;
	}

	public void setRom(int rom) {
		this.rom = rom;
	}

	public int getCamera() {
		return camera;
	}

	public void setCamera(int camera) {
		this.camera = camera;
	}

	public String getProcesesor() {
		return procesesor;
	}

	public void setProcesesor(String procesesor) {
		this.procesesor = procesesor;
	}

}
